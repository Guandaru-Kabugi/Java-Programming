public class LoopsForLoop {
    public static void main(String[] args){
        //Has three sections, the initializer, the condition, and the iteration
        for (int i = 0; i < 10; i++) { //we can also do i+=2
           System.out.println(i);
        }
        System.out.println("Reverse");
        for (int i = 10; i >=0 ; i--) {
           System.out.println(i);
        }
    }
}
